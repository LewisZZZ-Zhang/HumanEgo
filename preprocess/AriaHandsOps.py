    @staticmethod
    def print_summary_and_eval(aria_hands: AriaHands) -> None:
        """
        Print a formatted hand interaction and kinematics summary report to stdout.

        Reports: total frames, per-side hand presence count, grasp event counts,
        peak/mean wrist linear and angular velocity (from optimized trajectories),
        and an overall data quality rating based on hand presence coverage.

        Args:
            aria_hands (AriaHands): Full sequence hand tracking data.

        Returns:
            None. Outputs a bordered box report to stdout.
        """
        total_frames = len(aria_hands)
        r_frames = sum(1 for h in aria_hands.hands if h.hand_r)
        l_frames = sum(1 for h in aria_hands.hands if h.hand_l)

        lin_vels = []
        ang_vels = []
        for h_group in aria_hands.hands:
            for hand in [h_group.hand_r, h_group.hand_l]:
                if hand and hand.wrist_pose is not None:
                    lin_vels.append(np.linalg.norm(hand.wrist_lin_vel_opt_world))
                    ang_vels.append(np.linalg.norm(hand.wrist_ang_vel_opt_world))

        max_lin_v = max(lin_vels) if lin_vels else 0.0
        avg_lin_v = np.mean(lin_vels) if lin_vels else 0.0
        max_ang_v = max(ang_vels) if ang_vels else 0.0
        avg_ang_v = np.mean(ang_vels) if ang_vels else 0.0

        def count_grasps(hand_side: str) -> int:
            """Count grasp onset events (0→1 transitions) for one hand side."""
            states = [getattr(h, hand_side).grasp_state if getattr(h, hand_side) else 0 for h in aria_hands.hands]
            return sum(1 for i in range(1, len(states)) if states[i] == 1 and states[i-1] == 0)

        r_grasps = count_grasps('hand_r')
        l_grasps = count_grasps('hand_l')

        print("\n" + "╔" + "═" * 60 + "╗")
        print(f"║{'ARIA HAND INTERACTION & KINEMATICS REPORT':^60}║")
        print("╠" + "═" * 60 + "╣")
        print(f"║  - Total Frames       : {total_frames:<38} ║")
        print(f"║  - Hand Presence (R/L): {f'{r_frames} / {l_frames}':<38} ║")
        print(f"║  - Grasp Events (R/L) : {f'{r_grasps} / {l_grasps}':<38} ║")
        print(f"║ {'':<58} ║")
        print(f"║ [SMOOTHED KINEMATICS - OPTIMIZED] {'':<25} ║")
        print(f"║  - Peak Linear Speed  : {max_lin_v:>8.3f} m/s {'':<28} ║")
        print(f"║  - Mean Linear Speed  : {avg_lin_v:>8.3f} m/s {'':<28} ║")
        print(f"║  - Peak Angular Speed : {max_ang_v:>8.3f} rad/s {'':<26} ║")
        print(f"║  - Mean Angular Speed : {avg_ang_v:>8.3f} rad/s {'':<26} ║")

        # Quality score: average hand presence ratio across both hands
        quality_score = (r_frames + l_frames) / (2 * total_frames + 1e-6)
        quality = "EXCELLENT" if quality_score > 0.8 else "GOOD" if quality_score > 0.4 else "PARTIAL"
        print(f"║  - Overall Data Qual  : {quality:<38} ║")
        print("╚" + "═" * 60 + "╝\n")


    @staticmethod
    def _draw_skeleton(img: np.ndarray, hand: Any) -> np.ndarray:
        """
        Draw a 2D hand skeleton overlay on the image using Aria's official 21-keypoint layout.

        Uses mps.hand_tracking.kHandJointConnections for bone connectivity. Joint colors
        are per-finger and change between open-hand and grasp states. In grasp state,
        an extra outer glow ring is rendered on each joint for visual emphasis.

        Keypoint index convention (Aria MPS):
            0=ThumbCMC, 1=IndexMCP, 2=MiddleMCP, 3=RingMCP, 4=PinkyMCP,
            5=Wrist, 6-7=Thumb, 8-10=Index, 11-13=Middle, 14-16=Ring, 17-19=Pinky, 20=Palm.

        Args:
            img  (np.ndarray): BGR input image to draw on, shape (H, W, 3).
            hand (Any)       : AriaHandData object with:
                                 hand_keypoints_2d (np.ndarray, shape 21×2): 2D keypoints (px).
                                 grasp_state (int): 0=open, 1=closed/grasp.

        Returns:
            np.ndarray: Image with skeleton drawn in-place. Returned unchanged if
                        hand is None, keypoints are missing, or fewer than 21 points.
        """
        if hand is None or hand.hand_keypoints_2d is None:
            return img

        pts = hand.hand_keypoints_2d.astype(np.int32)
        is_grasp = (hand.grasp_state == 1)

        if len(pts) < 21: return img

        # Per-finger color mapping in BGR: [Thumb, Index, Middle, Ring, Pinky]
        if not is_grasp:
            # Pastel color scheme for open-hand state
            finger_colors = [
                (160, 160, 255), # Thumb  - Coral pink (reddish)
                (160, 255, 160), # Index  - Fresh green (greenish)
                (255, 210, 160), # Middle - Sky blue (bluish)
                (150, 250, 250), # Ring   - Bright yellow (yellowish)
                (250, 160, 250)  # Pinky  - Lavender purple (purplish)
            ]
        else:
            # Grasp state: every finger becomes pure red so the closed-hand
            # event is unambiguous at a glance.
            finger_colors = [(0, 0, 255)] * 5

        # Resolution-adaptive sizes (reference: 480-px-tall image).
        sc = img.shape[0] / 480.0
        bone_lw = max(1, int(round(1 * sc)))
        dot_r   = max(1, int(round(2 * sc)))
        glow_r  = max(2, int(round(3 * sc)))

        # Draw bone connections using official Aria joint topology (thin light gray)
        for conn in mps.hand_tracking.kHandJointConnections:
            idx1, idx2 = int(conn[0]), int(conn[1])
            p1, p2 = tuple(pts[idx1]), tuple(pts[idx2])
            if p1 != (0,0) and p2 != (0,0):
                cv2.line(img, p1, p2, (210, 210, 210), bone_lw, cv2.LINE_AA)

        # Map each keypoint index to its finger index for color lookup
        finger_map = {
            0:0, 6:0, 7:0,                # Thumb
            1:1, 8:1, 9:1, 10:1,          # Index
            2:2, 11:2, 12:2, 13:2,        # Middle
            3:3, 14:3, 15:3, 16:3,        # Ring
            4:4, 17:4, 18:4, 19:4         # Pinky
        }

        for i, pt in enumerate(pts):
            p = tuple(pt)
            if p == (0, 0): continue

            if is_grasp:
                # Grasp state: ALL 21 keypoints (including wrist/palm) are red.
                color = (0, 0, 255)
            else:
                # Open-hand state: keep per-finger pastels + special wrist/palm.
                if i == 5:
                    color = (255, 255, 255)
                elif i == 20:
                    color = (0, 0, 0)
                else:
                    f_idx = finger_map.get(i, 0)
                    color = finger_colors[f_idx]

            cv2.circle(img, p, dot_r, color, -1, cv2.LINE_AA)

            # Glow ring around joints in grasp state for visual emphasis
            if is_grasp:
                cv2.circle(img, p, glow_r, color, bone_lw, cv2.LINE_AA)

            # Thin gray outline on black/white wrist/palm in open-hand state only
            # (in grasp state they're already red — no outline needed).
            if not is_grasp and (i == 5 or i == 20):
                cv2.circle(img, p, dot_r, (150, 150, 150), bone_lw, cv2.LINE_AA)

        return img


    @staticmethod
    def _draw_axis(img: np.ndarray, pose: Optional[np.ndarray], k: np.ndarray, d: np.ndarray) -> np.ndarray:
        """
        Draw a stylized 3D coordinate axis gizmo (X/Y/Z arrows + base disk + laser pointer)
        projected onto the image at the location defined by the given pose.

        Gizmo elements:
          - Three arrowed axes: X=red, Y=green, Z=blue (OpenCV BGR convention).
          - A decorative base disk in the XZ plane for spatial grounding.
          - A laser pointer line extending along the X axis direction.
          - Semi-transparent blending (0.7 overlay + 0.3 original) for a clean look.

        Args:
            img  (np.ndarray)         : BGR input image, shape (H, W, 3).
            pose (np.ndarray or None) : 4×4 camera-space pose matrix (T_cam_hand).
                                        If None, the image is returned unchanged.
            k    (np.ndarray)         : Camera intrinsic matrix, shape (3, 3).
            d    (np.ndarray)         : Distortion coefficients, shape (4,) or (5,).

        Returns:
            np.ndarray: Image with the axis gizmo blended in.
                        Returns the original image if pose is None or origin is out of frame.
        """
        if pose is None: return img

        # 1. Extract rotation vector and translation vector
        r_vec, _ = cv2.Rodrigues(pose[:3, :3])
        t_vec = pose[:3, 3]

        # 2. Define points in 3D space
        axis_len_x = 0.06
        axis_len_y = 0.10
        axis_len_z = 0.06
        pointer_len = 0.10 # Pointer line length 12cm
        base_r = 0.025     # Base disk radius 2.5cm

        # Define: X, Y, Z axis endpoints, origin, pointer endpoint
        pts_3d = np.float32([
            [axis_len_x, 0, 0],          # X
            [0, axis_len_y, 0],          # Y (longer)
            [0, 0, axis_len_z],          # Z
            [0, 0, 0],                   # origin
            [pointer_len, 0, 0],         # pointer along X
        ])

        # Base disk: 16-point circle in the XZ plane
        num_circle_pts = 16
        circle_pts_3d = []
        for i in range(num_circle_pts):
            angle = 2 * np.pi * i / num_circle_pts
            # circle_pts_3d.append([base_r * np.cos(angle), base_r * np.sin(angle), 0]) # XY plane
            circle_pts_3d.append([base_r * np.cos(angle), 0, base_r * np.sin(angle)])# XZ plane
        pts_3d = np.append(pts_3d, np.float32(circle_pts_3d), axis=0)

        # 3. Project all 3D points to 2D pixel coordinates
        img_pts, _ = cv2.projectPoints(pts_3d, r_vec, t_vec, k, d)
        img_pts = [tuple(pt.ravel().astype(int)) for pt in img_pts]

        origin = img_pts[3]
        h, w = img.shape[:2]
        if not (0 <= origin[0] < w and 0 <= origin[1] < h):
            return img

        # --- Drawing start ---
        overlay = img.copy()

        # A. Draw base disk (semi-transparent ring in XZ plane)
        for i in range(num_circle_pts):
            p1 = img_pts[5 + i]
            p2 = img_pts[5 + (i + 1) % num_circle_pts]
            cv2.line(overlay, p1, p2, (200, 200, 200), 1, cv2.LINE_AA)

        # B. Draw laser pointer (long extension in X direction, cyan-yellow glow)
        laser_end = img_pts[4]
        cv2.line(overlay, origin, laser_end, (255, 255, 0), 1, cv2.LINE_AA)
        cv2.circle(overlay, laser_end, 2, (255, 255, 0), -1, cv2.LINE_AA)

        # C. Draw standard XYZ axes with arrows (BGR: X=red, Y=green, Z=blue)
        colors = [(0, 0, 255), (0, 255, 0), (255, 0, 0)]
        labels = ["X", "Y", "Z"]
        for i in range(3):
            cv2.arrowedLine(overlay, origin, img_pts[i], colors[i], 3, tipLength=0.3, line_type=cv2.LINE_AA)
            label_pos = (img_pts[i][0] + 8, img_pts[i][1] + 8)
            cv2.putText(overlay, labels[i], label_pos, cv2.FONT_HERSHEY_SIMPLEX,
                        0.6, colors[i], 2, cv2.LINE_AA)

        # D. Small white dot at the origin to mark the pivot point
        cv2.circle(overlay, origin, 3, (255, 255, 255), -1, cv2.LINE_AA)

        # Blend overlay with original for a semi-transparent effect
        return cv2.addWeighted(overlay, 0.7, img, 0.3, 0)


    @staticmethod
    def _draw_aria_hand_panel(
        img: np.ndarray,
        idx: int,
        hand: Any,
        is_right: bool,
        opt_v_limit: float,
    ) -> np.ndarray:
        """
        Draw a per-hand information panel (glass-style UI card) at the bottom corner of the image.

        The panel displays:
          - Hand side label and grasp state (OPEN / CLOSED), with a blinking red border on grasp.
          - Tracking confidence value.
          - Five per-finger flexion and abduction bars (normalized to [0, 1] range).
          - A wrist linear velocity gauge bar with current speed readout.

        Panel anchoring: right-hand → bottom-right corner; left-hand → bottom-left corner.
        All sizing is scaled proportionally to image width for resolution independence.

        Args:
            img      (np.ndarray): BGR image to draw on, shape (H, W, 3).
            idx      (int)       : Current frame index (used for blink animation timing).
            hand     (Any)       : AriaHandData object, or None if hand not detected.
            is_right (bool)      : True = right hand (bottom-right); False = left (bottom-left).
            opt_v_limit (float)

        Returns:
            np.ndarray: Image with the hand info panel drawn in-place.
        """
        is_blink = (idx // 5) % 2 == 0

        # Scale helpers: S() for pixel sizes, F() for font scale, T() for line thickness
        # Reference width 840 → sc=1.52 at 1280x960. Matches the SLAM panel's
        # layout reference so the bottom hand panels don't collide with the
        # top SLAM panels.
        img_h, img_w = img.shape[:2]
        sc = img_w / 840.0

        def S(val): return int(val * sc)
        def F(val): return max(0.3, val * sc)
        def T(val): return max(1, int(val * sc))

        w, h = S(200), S(180)
        margin = S(10)

        # Anchor position: right-hand panel at bottom-right, left-hand at bottom-left
        y = img_h - h - margin
        x = img_w - w - margin if is_right else margin

        # Joint angle key mapping: UI label → (flexion keys list, abduction key)
        mapping = {
            "Thu": (["Thumb_CMC_Flex", "Thumb_MCP_Flex", "Thumb_IP_Flex"], "Thumb_CMC_Abd"),
            "Ind": (["Index_MCP_Flex", "Index_PIP_Flex", "Index_DIP_Flex"], "Index_MCP_Abd"),
            "Mid": (["Middle_MCP_Flex", "Middle_PIP_Flex", "Middle_DIP_Flex"], "Middle_MCP_Abd"),
            "Rin": (["Ring_MCP_Flex", "Ring_PIP_Flex", "Ring_DIP_Flex"], "Ring_MCP_Abd"),
            "Pin": (["Pinky_MCP_Flex", "Pinky_PIP_Flex", "Pinky_DIP_Flex"], "Pinky_MCP_Abd")
        }

        # --- 2. State logic ---
        if hand is not None:
            is_grasp = (hand.grasp_state == 1)
            conf = hand.confidence
            state_str = "CLOSED" if is_grasp else "OPEN"
            col = (0, 0, 255) if is_grasp else (0, 191, 255)
            angle_data = hand.joint_angles.data if (hand.joint_angles and hand.joint_angles.data) else {}
        else:
            is_grasp = False; conf = 0.0; state_str = "N/A"
            col = (120, 120, 120); angle_data = {}

        # --- 3. Draw frosted glass background panel ---
        img = draw_glass_rect(img, (x, y), (x + w, y + h))
        if is_grasp and is_blink:
                cv2.rectangle(img, (x, y), (x + w, y + h), (0, 0, 255), T(2), cv2.LINE_AA)
                # Red dot at top-left corner as a grasp indicator
                cv2.circle(img, (x + S(15), y + S(18)), S(4), (0, 0, 255), -1, cv2.LINE_AA)

        # --- 4. Title row: centered hand side + grasp state label ---
        title = f"{'RIGHT' if is_right else 'LEFT'} HAND: {state_str}"
        font_scale = F(0.45)
        (t_w, _), _ = cv2.getTextSize(title, cv2.FONT_HERSHEY_DUPLEX, font_scale, T(1))
        t_x = x + (w - t_w) // 2
        cv2.putText(img, title, (t_x, y + S(25)), cv2.FONT_HERSHEY_DUPLEX, font_scale, col, T(1), cv2.LINE_AA)

        # --- 5. Confidence row ---
        conf_txt = f"CONFIDENCE: {conf:.2f}"
        # stat_txt = f"STATUS: {state_str}"
        cv2.putText(img, conf_txt, (x + S(10), y + S(45)), cv2.FONT_HERSHEY_DUPLEX, F(0.32), (200, 200, 200), T(1), cv2.LINE_AA)
        # cv2.putText(img, stat_txt, (x + S(105), y + S(45)), cv2.FONT_HERSHEY_DUPLEX, F(0.32), col, T(1), cv2.LINE_AA)

        # --- 6. Five-finger bars: top bar = flexion (norm. to 180°), bottom = abduction (norm. to 45°) ---
        finger_names = ["Thu", "Ind", "Mid", "Rin", "Pin"]
        max_bar_w = S(125)

        for i, ui_name in enumerate(finger_names):
            y_row = y + S(65 + i * 18)
            cv2.putText(img, ui_name, (x + S(10), y_row + S(8)), cv2.FONT_HERSHEY_DUPLEX, F(0.35), (255, 255, 255), T(1), cv2.LINE_AA)

            flex_keys, abd_key = mapping[ui_name]
            flex_sum = sum([angle_data.get(k, 0) for k in flex_keys])
            abd_val = angle_data.get(abd_key, 0)

            f_col = (0, 0, 255) if is_grasp else (200, 130, 60)
            f_w = int(min(flex_sum / 180.0, 1.0) * max_bar_w)
            a_w = int(min(abd_val / 45.0, 1.0) * max_bar_w)

            # Dark background slots for flexion (top) and abduction (bottom) bars
            cv2.rectangle(img, (x + S(55), y_row + S(1)), (x + S(55) + max_bar_w, y_row + S(4)), (50, 50, 50), -1)
            cv2.rectangle(img, (x + S(55), y_row + S(7)), (x + S(55) + max_bar_w, y_row + S(10)), (50, 50, 50), -1)

            if f_w > 0:
                cv2.rectangle(img, (x + S(55), y_row + S(1)), (x + S(55) + f_w, y_row + S(4)), f_col, -1, cv2.LINE_AA)
            if a_w > 0:
                cv2.rectangle(img, (x + S(55), y_row + S(7)), (x + S(55) + a_w, y_row + S(10)), (100, 200, 255), -1, cv2.LINE_AA)

        # --- 7. Velocity gauge: shows optimized wrist linear speed clamped to opt_v_limit ---
        if hand and hand.wrist_lin_vel_opt_world is not None:
            velocity_mag = np.linalg.norm(hand.wrist_lin_vel_opt_world)
        else:
            velocity_mag = 0.0

        v_y_row = y + S(158)
        cv2.putText(img, "VEL", (x + S(10), v_y_row + S(10)), cv2.FONT_HERSHEY_DUPLEX, F(0.35), (255, 255, 255), T(1), cv2.LINE_AA)

        max_vel_limit = opt_v_limit
        vel_ratio = min(velocity_mag / max_vel_limit, 1.0)
        vel_bar_w = int(vel_ratio * max_bar_w)

        # Dark gray background slot for velocity gauge
        cv2.rectangle(img, (x + S(55), v_y_row + S(2)), (x + S(55) + max_bar_w, v_y_row + S(10)), (40, 40, 40), -1)

        # Cyan below 1 m/s, yellow-green above (color shift signals high velocity)
        v_color = (255, 255, 0) if velocity_mag < 1.0 else (0, 255, 255)

        if vel_bar_w > 0:
            cv2.rectangle(img, (x + S(55), v_y_row + S(2)), (x + S(55) + vel_bar_w, v_y_row + S(10)), v_color, -1, cv2.LINE_AA)

        vel_text = f"{velocity_mag:.2f} m/s"
        cv2.putText(img, vel_text, (x + S(125), v_y_row - S(2)), cv2.FONT_HERSHEY_DUPLEX, F(0.3), v_color, T(1), cv2.LINE_AA)

        return img


    @staticmethod
    def _draw_opt_wrist_thumb_index_only(
        img: np.ndarray,
        aria_hand_data: Any,
        k: np.ndarray,
        d: np.ndarray,
        c2w: np.ndarray,
        draw_lines: bool = True,
        radius: int = 4,
        thickness: int = -1,
    ) -> np.ndarray:
        """
        Draw the 5 optimized key nodes and their skeletal connections, projected from
        world coordinates onto the camera image.

        Nodes drawn: wrist (W), thumb base, index base, thumb tip (T), index tip (I).
        Bone connectivity (when draw_lines=True):
            Wrist → Thumb Base → Thumb Tip
            Wrist → Index Base → Index Tip
        Fallback: if base knuckle coords are unavailable, connects Wrist → Tip directly.

        Node color coding (BGR):
            W (Wrist) = white | T (Thumb Tip) = red | I (Index Tip) = green | Base = cyan.

        Args:
            img           (np.ndarray): BGR image to draw on, shape (H, W, 3).
            aria_hand_data (Any)      : AriaHandData with optimized world-space fields:
                                          wrist_pose_opt_world, thumb/index_translation_opt_world,
                                          thumb/index_base_opt_world.
            k             (np.ndarray): Camera intrinsic matrix, shape (3, 3).
            d             (np.ndarray): Distortion coefficients.
            c2w           (np.ndarray): Camera-to-world 4×4 transform matrix.
            draw_lines    (bool)      : Whether to draw bone connection lines. Default True.
            radius        (int)       : Keypoint circle radius in pixels. Default 4.
            thickness     (int)       : Circle fill thickness (-1 = filled). Default -1.

        Returns:
            np.ndarray: Image with optimized keypoints and bones drawn in-place.
        """
        if aria_hand_data is None:
            return img

        T_w2c = np.linalg.inv(c2w)

        def _project_world_point(p_w: np.ndarray) -> Optional[Tuple[int, int]]:
            """Project a single world-space 3D point to image pixel coordinates (u, v)."""
            if p_w is None:
                return None
            p_w = np.asarray(p_w, dtype=np.float64).reshape(3)
            # Transform world → camera frame
            p_c = (T_w2c[:3, :3] @ p_w) + T_w2c[:3, 3]
            if p_c[2] <= 1e-6:  # Point is behind the camera
                return None
            rvec = np.zeros((3, 1), dtype=np.float64)
            tvec = np.zeros((3, 1), dtype=np.float64)
            uv, _ = cv2.projectPoints(p_c.reshape(1, 3), rvec, tvec, k, d)
            u, v = uv.reshape(2)
            h, w = img.shape[:2]
            if not (0 <= u < w and 0 <= v < h):
                return None
            return int(u), int(v)

        def _draw_one_hand(hand: Any) -> None:
            if hand is None:
                return

            # Fetch optimized world-space keypoint positions
            p_wrist_w = None
            if hand.wrist_pose_opt_world is not None:
                p_wrist_w = hand.wrist_pose_opt_world[:3, 3]

            p_t_tip_w = hand.thumb_translation_opt_world
            p_i_tip_w = hand.index_translation_opt_world

            # Base knuckle positions (may be absent in older data)
            p_t_base_w = getattr(hand, "thumb_base_opt_world", None)
            p_i_base_w = getattr(hand, "index_base_opt_world", None)

            # Project all key points to 2D pixel coordinates
            uv_wrist = _project_world_point(p_wrist_w)
            uv_t_tip = _project_world_point(p_t_tip_w)
            uv_i_tip = _project_world_point(p_i_tip_w)
            uv_t_base = _project_world_point(p_t_base_w)
            uv_i_base = _project_world_point(p_i_base_w)

            if uv_wrist is None and uv_t_tip is None and uv_i_tip is None:
                return

            C_WRIST = (255, 255, 255)  # White
            C_T_TIP = (0, 0, 255)      # Red
            C_I_TIP = (0, 255, 0)      # Green
            C_BASE  = (255, 255, 0)    # Cyan (for Knuckles)
            C_LINE  = (255, 255, 0)    # Cyan (for bones)

            if draw_lines:
                if uv_wrist and uv_t_base:
                    cv2.line(img, uv_wrist, uv_t_base, C_LINE, 1, cv2.LINE_AA)
                if uv_wrist and uv_i_base:
                    cv2.line(img, uv_wrist, uv_i_base, C_LINE, 1, cv2.LINE_AA)
                if uv_t_base and uv_t_tip:
                    cv2.line(img, uv_t_base, uv_t_tip, C_LINE, 1, cv2.LINE_AA)
                if uv_i_base and uv_i_tip:
                    cv2.line(img, uv_i_base, uv_i_tip, C_LINE, 1, cv2.LINE_AA)

                # Fallback: if Base not extracted (old data), connect Wrist -> Tip directly
                if uv_t_base is None and uv_wrist and uv_t_tip:
                    cv2.line(img, uv_wrist, uv_t_tip, C_LINE, 1, cv2.LINE_AA)
                if uv_i_base is None and uv_wrist and uv_i_tip:
                    cv2.line(img, uv_wrist, uv_i_tip, C_LINE, 1, cv2.LINE_AA)

            # Draw wrist node with outline and label
            if uv_wrist is not None:
                cv2.circle(img, uv_wrist, radius + 1, (0, 0, 0), 1, cv2.LINE_AA)
                cv2.circle(img, uv_wrist, radius, C_WRIST, thickness, cv2.LINE_AA)
                cv2.putText(img, "W", (uv_wrist[0] + 6, uv_wrist[1] - 6),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.5, C_WRIST, 1, cv2.LINE_AA)

            # Draw thumb knuckle base (cyan, with white core dot)
            if uv_t_base is not None:
                cv2.circle(img, uv_t_base, radius, C_BASE, thickness, cv2.LINE_AA)
                cv2.circle(img, uv_t_base, 2, (255, 255, 255), -1, cv2.LINE_AA)

            # Draw index knuckle base (cyan, with white core dot)
            if uv_i_base is not None:
                cv2.circle(img, uv_i_base, radius, C_BASE, thickness, cv2.LINE_AA)
                cv2.circle(img, uv_i_base, 2, (255, 255, 255), -1, cv2.LINE_AA)

            # Draw thumb tip (red, T label)
            if uv_t_tip is not None:
                cv2.circle(img, uv_t_tip, radius, C_T_TIP, thickness, cv2.LINE_AA)
                cv2.putText(img, "T", (uv_t_tip[0] + 6, uv_t_tip[1] - 6),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.5, C_T_TIP, 1, cv2.LINE_AA)

            # Draw index tip (green, I label)
            if uv_i_tip is not None:
                cv2.circle(img, uv_i_tip, radius, C_I_TIP, thickness, cv2.LINE_AA)
                cv2.putText(img, "I", (uv_i_tip[0] + 6, uv_i_tip[1] - 6),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.5, C_I_TIP, 1, cv2.LINE_AA)

        _draw_one_hand(aria_hand_data)

        return img


