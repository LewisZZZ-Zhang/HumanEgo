"""
====================================================================================================
Aria Hand Tracking Visualization & Analysis Operations (AriaHandsOps.py)
====================================================================================================

Description:
    This module provides a collection of static utility methods for visualizing and analyzing
    processed hand tracking data from Project Aria Machine Perception Services (MPS).
    It operates on pre-processed AriaHandsData / AriaHands structures and covers two major areas:
    (1) real-time per-frame visualization overlaid on camera images, and (2) offline batch
    analysis plots that compare raw vs. optimized kinematic trajectories over the full sequence.

Core Functionalities:
    1.  Skeleton Visualization: Draws 2D hand skeletons on camera frames using Aria's official
        21-keypoint layout, with per-finger color coding and grasp-state visual feedback.
    2.  Pose Axis Rendering: Projects 3D wrist/midpoint pose axes (X/Y/Z) onto camera images
        using OpenCV's projectPoints, with a decorative base disk and laser pointer effect.
    3.  HUD Overlay: Renders an informational heads-up display showing thumb-index pinch
        distance, wrist-midpoint arm length, and grasp state indicators on each frame.
    4.  Panel UI: Draws per-hand info panels (confidence, joint angle bars, velocity gauge)
        at the bottom corners of the visualization frame.
    5.  Analysis Plots: Generates two comprehensive per-hand (left/right) matplotlib figures
        covering 20+ sub-panels: position/velocity/acceleration trajectories, 3D/2D spatial
        paths, pose deviation (Raw vs. Opt), orientation axes, and a statistics text report.

Generated Outputs:
    📊 aria_hands_analysis_r.png  -- Right-hand full kinematic analysis report figure.
    📊 aria_hands_analysis_l.png  -- Left-hand full kinematic analysis report figure.

Technical Specifics:
    - All optimized ("Opt") data refers to trajectories smoothed via Savitzky-Golay / EMA
      filtering applied upstream in the pipeline (AriaHands.py).
    - Raw ("Raw") data is the direct MPS closed-loop world-frame output before smoothing.
    - Color convention in plots: Red = Raw, Green = Optimized.
    - Coordinate Frame: MPS Closed Loop World frame for 3D; image pixel frame for 2D.
====================================================================================================
"""

