    @staticmethod
    def draw_aria_hands_skeleton(
        img: np.ndarray,
        aria_hands_data: AriaHandsData,
        k: np.ndarray,
        d: np.ndarray,
        c2w: np.ndarray,
        grasp_threshold: float,
        full_skeleton: bool = False,
    ) -> np.ndarray:
        """
        Full per-frame visualization entry point: renders the complete hand overlay for
        both hands including the advanced HUD, midpoint pose axis gizmo, and optimized keypoints.

        For each detected hand (right and/or left), applies in order:
          1. _draw_advanced_hud()               — pinch/arm distance labels and connecting lines.
          2. _draw_axis()                        — XYZ pose axis gizmo at the midpoint frame.
          3. _draw_opt_wrist_thumb_index_only()  — optimized wrist/thumb/index node overlay.

        Args:
            img             (np.ndarray)   : BGR image to draw on, shape (H, W, 3).
            aria_hands_data (AriaHandsData): Container with hand_r and hand_l fields.
            k               (np.ndarray)   : Camera intrinsic matrix, shape (3, 3).
            d               (np.ndarray)   : Distortion coefficients.
            c2w             (np.ndarray)   : Camera-to-world 4×4 transform matrix.
            grasp_threshold (float)

        Returns:
            np.ndarray: Image with all visualization layers rendered for both hands.
        """
        for hand in [aria_hands_data.hand_r, aria_hands_data.hand_l]:
            if hand is not None:
                img = AriaHandsOps._draw_advanced_hud(img, hand, k, d, c2w, grasp_threshold)
                img = AriaHandsOps._draw_axis(img, np.linalg.inv(c2w) @ hand.midpoint_pose_opt_world, k, d)
                if full_skeleton:
                    # Render the full 21-keypoint hand skeleton (all fingers + bones)
                    img = AriaHandsOps._draw_skeleton(img, hand)
                else:
                    # Render only the simplified parallel-jaw view (wrist + thumb + index)
                    img = AriaHandsOps._draw_opt_wrist_thumb_index_only(img, hand, k, d, c2w)
        return img
        