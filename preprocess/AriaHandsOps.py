    @staticmethod
    def _set_frame_time_ticks(ax: Any, frames: np.ndarray, dt: float) -> None:
        """
        Set x-axis tick labels on a matplotlib Axes as "frame index + elapsed time (s)".

        Each tick shows two lines: the integer frame number on top and the corresponding
        timestamp (frame_id * dt) in seconds below, providing intuitive temporal context.

        Args:
            ax     (matplotlib.axes.Axes): The subplot axes to modify.
            frames (np.ndarray)          : 1-D array of frame indices, shape (N,).
            dt     (float)               : Time interval per frame in seconds (1 / fps).

        Returns:
            None
        """
        n = len(frames)
        if n <= 1:
            return
        k = max(2, ANALYSIS_XTICK_NUM)
        tick_ids = np.linspace(0, n - 1, k).astype(int)
        tick_ids = np.unique(tick_ids)

        ax.set_xticks(frames[tick_ids])
        labels = []
        for fid in frames[tick_ids]:
            t = fid * dt
            labels.append(f"{int(fid)}\n{t:.2f}s")
        ax.set_xticklabels(labels, fontsize=9)


    @staticmethod
    def _safe_nan_array(vals: Any) -> np.ndarray:
        """
        Safely convert an arbitrary value or sequence to a float numpy array.

        Handles scalar inputs by wrapping them in a 1-element array, ensuring
        downstream array operations (e.g., indexing, nanmean) are always valid.

        Args:
            vals (Any): A scalar, list, or array-like of numeric values.

        Returns:
            np.ndarray: A float64 numpy array. Scalars become shape (1,).
        """
        arr = np.array(vals, dtype=float)
        if arr.ndim == 0:
            arr = np.array([arr], dtype=float)
        return arr


    @staticmethod
    def _collect_series_single_hand(aria_hands: AriaHands, is_right: bool) -> Dict:
        """
        Extract all per-frame time-series data for one hand from an AriaHands sequence,
        returning a flat dictionary of arrays ready for analysis plotting.

        Iterates over every frame, pulling out confidence, grasp state, raw/optimized 3D
        positions, pose matrices, velocities, 2D projections, and orientation axes.
        Missing frames are represented as NaN. A smoothed thumb-index distance curve
        (dist_opt) and 2D midpoint trajectory (mid2d_opt) are also computed here.

        Args:
            aria_hands (AriaHands): Full hand tracking sequence with per-frame AriaHandsData.
            is_right   (bool)     : True to collect the right hand; False for left hand.

        Returns:
            Dict: A dictionary (from locals()) containing the following numpy arrays:
                - frames          (N,)    : Frame index array [0..N-1].
                - conf            (N,)    : Tracking confidence per frame [0, 1].
                - grasp           (N,)    : Binary grasp state (0=open, 1=closed).
                - dist_raw        (N,)    : Raw thumb-index 3D distance in meters.
                - dist_opt        (N,)    : Smoothed thumb-index distance in meters.
                - mid_raw_xyz     (N, 3)  : Raw midpoint world position (m).
                - mid_opt_xyz     (N, 3)  : Optimized midpoint world position (m).
                - wrist_raw_xyz   (N, 3)  : Raw wrist world position (m).
                - wrist_opt_xyz   (N, 3)  : Optimized wrist world position (m).
                - wrist_pose_raw/opt  (List[4x4 or None]): Per-frame wrist pose matrices.
                - mid_pose_raw/opt    (List[4x4 or None]): Per-frame midpoint pose matrices.
                - wrist/mid_lin/ang_raw/opt (N,): Velocity magnitudes (m/s, rad/s).
                - d_mid2w_raw/opt (N,)    : Midpoint-to-wrist distance in meters.
                - mid2d_raw       (N, 2)  : Raw 2D midpoint in image pixels.
                - mid2d_opt       (N, 2)  : Smoothed 2D midpoint in image pixels.
                - mid_ax_raw/opt  (N, 3, 3): Orientation axis columns in world frame.
        """
        hand_attr = "hand_r" if is_right else "hand_l"
        n = len(aria_hands)
        frames = np.arange(n)
        conf, grasp = np.zeros(n), np.zeros(n)

        # Position and pose containers (NaN = missing frame)
        dist_raw = np.full(n, np.nan)
        mid_raw_xyz, mid_opt_xyz = np.full((n, 3), np.nan), np.full((n, 3), np.nan)
        wrist_raw_xyz, wrist_opt_xyz = np.full((n, 3), np.nan), np.full((n, 3), np.nan)
        wrist_pose_raw, wrist_pose_opt = [None] * n, [None] * n
        mid_pose_raw, mid_pose_opt = [None] * n, [None] * n
        mid2d_raw, mid2d_opt = np.full((n, 2), np.nan), np.full((n, 2), np.nan)

        # Velocity containers (NaN = missing frame)
        wrist_lin_raw, wrist_lin_opt = np.full(n, np.nan), np.full(n, np.nan)
        wrist_ang_raw, wrist_ang_opt = np.full(n, np.nan), np.full(n, np.nan)
        mid_lin_raw, mid_lin_opt = np.full(n, np.nan), np.full(n, np.nan)
        mid_ang_raw, mid_ang_opt = np.full(n, np.nan), np.full(n, np.nan)
        d_mid2w_raw, d_mid2w_opt = np.full(n, np.nan), np.full(n, np.nan)
        mid_ax_raw, mid_ax_opt = np.full((n, 3, 3), np.nan), np.full((n, 3, 3), np.nan)

        for i, frame_data in enumerate(aria_hands.hands):
            h = getattr(frame_data, hand_attr)
            if h is None: continue

            conf[i] = float(h.confidence) if h.confidence is not None else 0.0
            grasp[i] = int(h.grasp_state)

            if h.hand_keypoints_3d is not None:
                dist_raw[i] = np.linalg.norm(h.hand_keypoints_3d[0] - h.hand_keypoints_3d[1])

            if h.midpoint_translation_raw_world is not None: mid_raw_xyz[i, :] = h.midpoint_translation_raw_world
            if h.midpoint_translation_opt_world is not None: mid_opt_xyz[i, :] = h.midpoint_translation_opt_world
            if h.wrist_pose_raw_world is not None:
                wrist_raw_xyz[i, :] = h.wrist_pose_raw_world[:3, 3]
                wrist_pose_raw[i] = h.wrist_pose_raw_world
            if h.wrist_pose_opt_world is not None:
                wrist_opt_xyz[i, :] = h.wrist_pose_opt_world[:3, 3]
                wrist_pose_opt[i] = h.wrist_pose_opt_world

            mid_pose_raw[i] = h.midpoint_pose_raw_world
            mid_pose_opt[i] = h.midpoint_pose_opt_world

            if h.midpoint_pose_raw_world is not None and h.wrist_pose_raw_world is not None:
                d_mid2w_raw[i] = np.linalg.norm(h.midpoint_pose_raw_world[:3, 3] - h.wrist_pose_raw_world[:3, 3])
            if h.midpoint_pose_opt_world is not None and h.wrist_pose_opt_world is not None:
                d_mid2w_opt[i] = np.linalg.norm(h.midpoint_pose_opt_world[:3, 3] - h.wrist_pose_opt_world[:3, 3])

            # Velocity magnitude extraction (None → NaN to preserve array alignment)
            wrist_lin_raw[i] = np.linalg.norm(h.wrist_lin_vel_raw_world) if h.wrist_lin_vel_raw_world is not None else np.nan
            wrist_lin_opt[i] = np.linalg.norm(h.wrist_lin_vel_opt_world) if h.wrist_lin_vel_opt_world is not None else np.nan
            wrist_ang_raw[i] = np.linalg.norm(h.wrist_ang_vel_raw_world) if h.wrist_ang_vel_raw_world is not None else np.nan
            wrist_ang_opt[i] = np.linalg.norm(h.wrist_ang_vel_opt_world) if h.wrist_ang_vel_opt_world is not None else np.nan
            mid_lin_raw[i] = np.linalg.norm(h.midpoint_lin_vel_raw_world) if h.midpoint_lin_vel_raw_world is not None else np.nan
            mid_lin_opt[i] = np.linalg.norm(h.midpoint_lin_vel_opt_world) if h.midpoint_lin_vel_opt_world is not None else np.nan
            mid_ang_raw[i] = np.linalg.norm(h.midpoint_ang_vel_raw_world) if h.midpoint_ang_vel_raw_world is not None else np.nan
            mid_ang_opt[i] = np.linalg.norm(h.midpoint_ang_vel_opt_world) if h.midpoint_ang_vel_opt_world is not None else np.nan

            # 2D midpoint: average of thumb tip (index 0) and index tip (index 1) keypoints
            if h.hand_keypoints_2d is not None and len(h.hand_keypoints_2d) >= 2:
                if not (np.allclose(h.hand_keypoints_2d[0], 0) or np.allclose(h.hand_keypoints_2d[1], 0)):
                    mid2d_raw[i, :] = (h.hand_keypoints_2d[0] + h.hand_keypoints_2d[1]) / 2.0

            # Extract orientation axis columns (x, y, z) from the midpoint rotation matrix
            for pose_obj, ax_container in [(mid_pose_raw[i], mid_ax_raw), (mid_pose_opt[i], mid_ax_opt)]:
                if pose_obj is not None:
                    R_mat = pose_obj[:3, :3]
                    for j in range(3): ax_container[i, j, :] = R_mat[:, j]

        # Post-process: interpolate NaN gaps and apply box smoothing for cleaner plot curves
        dist_opt = AriaHandsOps._interpolate_and_smooth(dist_raw)
        mid2d_opt = np.stack([AriaHandsOps._interpolate_and_smooth(mid2d_raw[:, d]) for d in range(2)], axis=1)

        return locals()


    @staticmethod
    def _interpolate_and_smooth(v: np.ndarray, size: int = 5) -> np.ndarray:
        """
        Fill NaN gaps via linear interpolation, then apply a uniform (box) filter for smoothing.

        Used to produce visually clean curves in analysis plots where missing detections
        (NaN frames) would otherwise create discontinuities.

        Args:
            v    (np.ndarray): 1-D input array potentially containing NaN values, shape (N,).
            size (int)       : Kernel size for uniform_filter1d (box smoothing window).
                               Default is 5.

        Returns:
            np.ndarray: Smoothed 1-D float array of the same shape (N,).
                        Returned unchanged if fewer than 2 valid (non-NaN) samples exist.
        """
        v = v.copy()
        nm = ~np.isnan(v)
        if np.sum(nm) >= 2:
            v[~nm] = np.interp(np.flatnonzero(~nm), np.flatnonzero(nm), v[nm])
            return uniform_filter1d(v, size=size)
        return v


    @staticmethod
    def _compute_pose_deviation(
        raw_list: List[Optional[np.ndarray]],
        opt_list: List[Optional[np.ndarray]]
    ) -> Tuple[np.ndarray, np.ndarray]:
        """
        Compute per-frame position error (m) and rotation error (degrees) between
        raw and optimized pose matrices.

        For each valid frame pair:
          - Position error = Euclidean distance between translation vectors (||t_raw - t_opt||).
          - Rotation error = rotation angle (deg) of the relative rotation R_raw.T @ R_opt.

        Args:
            raw_list (List[Optional[np.ndarray]]): N-length list of raw 4×4 pose matrices
                                                   (or None for missing frames).
            opt_list (List[Optional[np.ndarray]]): N-length list of optimized 4×4 pose matrices
                                                   (or None for missing frames).

        Returns:
            Tuple[np.ndarray, np.ndarray]:
                - pos_err (np.ndarray, shape N): Per-frame position deviation in meters.
                - rot_err (np.ndarray, shape N): Per-frame rotation deviation in degrees.
                  Both arrays contain NaN for frames where either pose is missing.
        """
        N = len(raw_list)
        pos_err = np.full(N, np.nan)
        rot_err = np.full(N, np.nan)

        for i in range(N):
            if raw_list[i] is None or opt_list[i] is None:
                continue
            pr = raw_list[i][:3, 3]
            po = opt_list[i][:3, 3]
            pos_err[i] = np.linalg.norm(pr - po)

            rr = raw_list[i][:3, :3]
            ro = opt_list[i][:3, :3]
            r_rel = rr.T @ ro
            ang = np.linalg.norm(R.from_matrix(r_rel).as_rotvec())
            rot_err[i] = np.degrees(ang)

        return pos_err, rot_err


    @staticmethod
    def _compute_acc_from_vel_mag(vel_mag: np.ndarray, dt: float) -> np.ndarray:
        """
        Estimate scalar acceleration magnitude via first-order finite difference of velocity.

        For each consecutive valid pair: acc[i] = (vel_mag[i] - vel_mag[i-1]) / dt.
        Used to derive acceleration time series from velocity arrays for analysis plots.

        Args:
            vel_mag (np.ndarray): 1-D velocity magnitude array (m/s or rad/s), shape (N,).
                                  May contain NaN for frames with missing data.
            dt      (float)     : Time step between frames in seconds (1 / fps).

        Returns:
            np.ndarray: 1-D acceleration magnitude array, shape (N,), with NaN where
                        either the current or previous velocity sample is missing.
        """
        v = vel_mag.copy()
        acc = np.full_like(v, np.nan)
        for i in range(1, len(v)):
            if np.isnan(v[i]) or np.isnan(v[i-1]):
                continue
            acc[i] = (v[i] - v[i-1]) / dt
        return acc


    @staticmethod
    def save_hands_analysis_plots_two(
        aria_hands: AriaHands,
        save_dir: str,
        dt: float,
        cfg: Any
    ) -> None:
        """
        Generate and save two comprehensive kinematic analysis figures (right & left hand).

        Each figure is a 7×4 subplot grid covering:
          Row 0: Confidence | Grasp + Thumb-Index Dist | Midpoint X | Midpoint Y
          Row 1: Midpoint Z | Mid Pos Deviation | Mid Rot Deviation | Mid Acc Deviation
          Row 2: Mid 3D Trajectory | Mid 2D Trajectory | Wrist X | Wrist Y
          Row 3: Wrist Z | Wrist Pos Deviation | Wrist Rot Deviation | Wrist Acc Deviation
          Row 4: Wrist 3D Trajectory | Wrist 2D Proxy | Mid↔Wrist Dist | Wrist |v|
          Row 5: Wrist |ω| | Midpoint |v| | Midpoint |ω| | (empty)
          Row 6: Midpoint X-axis | Midpoint Y-axis | Midpoint Z-axis | Text Report

        Each subplot overlays raw (faded) vs. optimized (solid) trajectories for comparison.

        Args:
            aria_hands (AriaHands): Full sequence hand tracking data.
            save_dir   (str)      : Output directory. Created if it does not exist.
            dt         (float)    : Per-frame time step in seconds (1 / fps).
            cfg        (Any)      : Config object with attributes:
                                      cfg.grasp_threshold (float): Pinch distance threshold (m).
                                      cfg.OPT_V_LIMIT     (float): Linear velocity limit (m/s).
                                      cfg.opt_w_limit     (float): Angular velocity limit (rad/s).

        Returns:
            None. Saves files:
                {save_dir}/aria_hands_analysis_r.png
                {save_dir}/aria_hands_analysis_l.png
        """
        os.makedirs(save_dir, exist_ok=True)
        warnings.filterwarnings("ignore", category=UserWarning, module="matplotlib")

        def _safe_legend(ax: Any, fontsize: int = 8, ncol: int = 1) -> None:
            """Draw legend only when labeled artists exist, to suppress matplotlib warnings."""
            handles, labels = ax.get_legend_handles_labels()
            if handles: ax.legend(loc="upper right", fontsize=fontsize, ncol=ncol)

        def _plot_axis_vec(ax: Any, v_raw: np.ndarray, v_opt: np.ndarray, title: str) -> None:
            """
            Plot the X/Y/Z world-frame components of a single orientation axis over time.

            Args:
                ax    (Axes)      : Target subplot axes.
                v_raw (np.ndarray): Raw axis component vectors, shape (N, 3).
                v_opt (np.ndarray): Optimized axis component vectors, shape (N, 3).
                title (str)       : Subplot title string.
            """
            ax.grid(True, alpha=0.35)

            labels = ["Xcomp", "Ycomp", "Zcomp"]
            for j in range(3):
                ax.plot(frames, v_raw[:, j], alpha=ANALYSIS_ALPHA_RAW, linewidth=ANALYSIS_LINEWIDTH_AUX,
                        label=f"Raw {labels[j]}")
                ax.plot(frames, v_opt[:, j], alpha=ANALYSIS_ALPHA_OPT, linewidth=ANALYSIS_LINEWIDTH_MAIN,
                        label=f"Opt {labels[j]}")

            ax.set_title(title, fontsize=ANALYSIS_SUBTITLE_FONTSIZE, fontweight='bold')
            ax.set_ylabel("axis component (unit)")
            ax.set_xlabel("Frame\nTime (s)")
            ax.set_ylim(-1.05, 1.05)
            AriaHandsOps._set_frame_time_ticks(ax, frames, dt)
            ax.legend(loc="upper right", fontsize=8, ncol=2)

        def _plot_ts(ax: Any, y_raw: Optional[np.ndarray] = None, y_opt: Optional[np.ndarray] = None,
                        label_raw: str = "Raw", label_opt: str = "Optimized",
                        ylabel: str = "", title: str = "",
                        show_limit: Optional[float] = None, limit_label: str = "Limit") -> None:
            """
            Generic time-series subplot: plots raw (faded) and optimized (solid) curves
            with an optional dashed horizontal limit line.

            Args:
                ax          (Axes)           : Target subplot axes.
                y_raw       (np.ndarray|None): Raw values, shape (N,). Skipped if None.
                y_opt       (np.ndarray|None): Optimized values, shape (N,). Skipped if None.
                label_raw   (str)            : Legend label for the raw curve.
                label_opt   (str)            : Legend label for the optimized curve.
                ylabel      (str)            : Y-axis label string.
                title       (str)            : Subplot title string.
                show_limit  (float|None)     : If given, draws a dashed horizontal limit line.
                limit_label (str)            : Legend label for the limit line.
            """
            ax.grid(True, alpha=0.35)
            if y_raw is not None:
                ax.plot(frames, y_raw, color=C_RAW, alpha=ANALYSIS_ALPHA_RAW, linewidth=ANALYSIS_LINEWIDTH_AUX, label=label_raw)
            if y_opt is not None:
                ax.plot(frames, y_opt, color=C_OPT, alpha=ANALYSIS_ALPHA_OPT, linewidth=ANALYSIS_LINEWIDTH_MAIN, label=label_opt)
            if show_limit is not None:
                ax.axhline(show_limit, linestyle="--", linewidth=1.2, color="#444444", alpha=0.9, label=limit_label)
            ax.set_title(title, fontsize=ANALYSIS_SUBTITLE_FONTSIZE, fontweight='bold')
            ax.set_ylabel(ylabel)
            ax.set_xlabel("Frame\nTime (s)")
            AriaHandsOps._set_frame_time_ticks(ax, frames, dt)
            ax.legend(loc="upper right", fontsize=9)

        for is_right in [True, False]:
            side_name = "r" if is_right else "l"
            save_path = os.path.join(save_dir, f"aria_hands_analysis_{side_name}.png")

            data = AriaHandsOps._collect_series_single_hand(aria_hands, is_right=is_right)

            frames = data["frames"]
            title_side = "RIGHT HAND" if is_right else "LEFT HAND"

            plt.style.use('seaborn-v0_8-whitegrid')
            fig = plt.figure(figsize=ANALYSIS_FIGSIZE)
            gs = fig.add_gridspec(ANALYSIS_GRID_ROWS, ANALYSIS_GRID_COLS, hspace=0.35, wspace=0.22)

            # Unified color scheme (professional and high contrast)
            C_CONF = "#2E86C1"
            C_GRASP = "#E74C3C"
            C_RAW = "#E74C3C"
            C_OPT = "#2ECC71"
            C_AUX = "#F1C40F"
            C_DEV = "#8E44AD"
            C_ROT = "#8B4513"

            fig.suptitle(
                f"ARIA HAND ADVANCED ANALYSIS REPORT - {title_side}",
                fontsize=ANALYSIS_TITLE_FONTSIZE,
                fontweight='bold'
            )

            # -------------------------
            # 1) Confidence
            # -------------------------
            ax = fig.add_subplot(gs[0, 0])
            ax.grid(True, alpha=0.35)
            ax.plot(frames, data["conf"], color=C_CONF, linewidth=ANALYSIS_LINEWIDTH_MAIN, alpha=0.95, label="Confidence")
            ax.set_title("Hand Tracking Confidence over Time", fontsize=ANALYSIS_SUBTITLE_FONTSIZE, fontweight='bold')
            ax.set_ylim(0, 1.05)
            ax.set_ylabel("Confidence")
            ax.set_xlabel("Frame\nTime (s)")
            AriaHandsOps._set_frame_time_ticks(ax, frames, dt)
            ax.legend(loc="upper right", fontsize=9)

            # -------------------------
            # 2) Grasp action + thumb-index distance (raw/opt) + threshold
            # -------------------------
            ax = fig.add_subplot(gs[0, 1])
            ax.grid(True, alpha=0.35)

            # Grasp state (0/1) as a filled step area
            ax.fill_between(frames, data["grasp"], color=C_GRASP, alpha=0.18, step="pre", label="Grasp State (Closed=1)")

            # Overlay thumb-index distance on a secondary y-axis
            ax2 = ax.twinx()
            ax2.plot(frames, data["dist_raw"], color=C_RAW, alpha=ANALYSIS_ALPHA_RAW, linewidth=ANALYSIS_LINEWIDTH_AUX, label="Thumb-Index Dist (Raw)")
            ax2.plot(frames, data["dist_opt"], color=C_OPT, alpha=ANALYSIS_ALPHA_OPT, linewidth=ANALYSIS_LINEWIDTH_MAIN, label="Thumb-Index Dist (Opt)")
