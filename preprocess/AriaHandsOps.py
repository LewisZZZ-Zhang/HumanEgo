    @staticmethod
    def _draw_advanced_hud(
        img: np.ndarray,
        hand: Any,
        k: np.ndarray,
        d: np.ndarray,
        c2w: np.ndarray,
        grasp_threshold: float,
    ) -> np.ndarray:
        """
        Draw an advanced HUD overlay showing pinch distance, arm length, and grasp state
        as annotated connection lines and floating label boxes.

        All displayed distances and connecting lines are computed exclusively from
        smoothed (Opt) world-space coordinates for temporal stability.

        Overlay elements:
          - Dashed line: thumb tip ↔ index tip (color changes with grasp state).
          - Solid line: midpoint → wrist.
          - Floating label near midpoint: "DIST(OPT): Xm" + grasp threshold.
          - Floating label near wrist: "ARM(OPT): Xm" (midpoint-to-wrist distance).
          - Leader lines connecting labels to their anchor points.

        Args:
            img  (np.ndarray): BGR image to draw on, shape (H, W, 3).
            hand (Any)       : AriaHandData with optimized world fields:
                                 wrist_pose_opt_world, thumb/index_translation_opt_world,
                                 midpoint_translation_opt_world, grasp_state.
            k    (np.ndarray): Camera intrinsic matrix, shape (3, 3).
            d    (np.ndarray): Distortion coefficients.
            c2w  (np.ndarray): Camera-to-world 4×4 transform matrix.
            grasp_threshold (float)

        Returns:
            np.ndarray: Image with HUD elements drawn in-place.
                        Returns the original image unchanged if hand is None,
                        wrist_pose_opt_world is None, or any key point is out of view.
        """
        def _project_world_to_2d(
            p_w: Optional[np.ndarray],
            c2w: np.ndarray,
            k: np.ndarray,
            d: np.ndarray
        ) -> Optional[np.ndarray]:
            """Project a world-space 3D point to 2D pixel coordinates as an int32 array."""
            if p_w is None: return None
            T_w2c = np.linalg.inv(c2w)
            p_c = (T_w2c[:3, :3] @ p_w) + T_w2c[:3, 3]
            if p_c[2] <= 1e-6: return None
            # rvec and tvec are zero because p_c is already in the camera frame
            uv, _ = cv2.projectPoints(p_c.reshape(1, 3), np.zeros(3), np.zeros(3), k, d)
            u, v = uv.reshape(2)
            return np.array([u, v], dtype=np.int32)

        def _draw_dotted_line(
            img: np.ndarray,
            p1: np.ndarray,
            p2: np.ndarray,
            color: Tuple[int, int, int],
            thickness: int = 1,
            gap: int = 4
        ) -> None:
            """Draw a dashed line between two 2D points by alternating drawn/skipped segments."""
            dist = np.linalg.norm(p1 - p2)
            if dist < 1e-3: return
            for i in range(0, int(dist), gap * 2):
                start = tuple((p1 + (p2 - p1) * (i / dist)).astype(np.int32))
                end = tuple((p1 + (p2 - p1) * ((i + gap) / dist)).astype(np.int32))
                cv2.line(img, start, end, color, thickness, cv2.LINE_AA)

        # 1. Safety check: ensure optimized data exists
        if hand is None or hand.wrist_pose_opt_world is None:
            return img

        # 2. Extract optimized world coordinates 3D points
        p_wrist_w = hand.wrist_pose_opt_world[:3, 3]
        p_thumb_w = hand.thumb_translation_opt_world
        p_index_w = hand.index_translation_opt_world
        p_mid_w   = hand.midpoint_translation_opt_world

        # 3. Compute optimized physical distances (for text display)
        grasp_dist_3d = np.linalg.norm(p_thumb_w - p_index_w)
        wrist_dist_3d = np.linalg.norm(p_mid_w - p_wrist_w)

        # 4. Project 3D points to 2D pixel coordinates
        uv_wrist = _project_world_to_2d(p_wrist_w, c2w, k, d)
        uv_thumb = _project_world_to_2d(p_thumb_w, c2w, k, d)
        uv_index = _project_world_to_2d(p_index_w, c2w, k, d)
        uv_mid   = _project_world_to_2d(p_mid_w, c2w, k, d)

        # If keypoints fail to project (e.g., out of view), skip HUD drawing
        if uv_wrist is None or uv_thumb is None or uv_index is None or uv_mid is None:
            return img

        # 5. UI color and style settings
